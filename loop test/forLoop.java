import java.util.Scanner;

import java.util.Scanner;

public class forLoop {
    public static void main(String args[]){
        System.out.println("Enter the String:");
        Scanner obj=new Scanner(System.in);
        String name = obj.next();
        for(int i=0;i<=5;i++){
            System.out.println(name);
        }
    }
    
}
